/** Created by Randy Yin
 * This class is responsible for keeping track of teachers id, name, salary
 */

public class Teacher {

    private int id;
    private String name;
    private int salary;

    //id, name, and salary will be unique for each teacher
    public Teacher(int id, String name, int salary) {
        this.id = id;
        this.name = name;
        this.salary = salary;
    }



}
